package knightangle;

import java.util.Scanner;

public class While {
			public static void main(String[] args) {
				Scanner hill=new  Scanner(System.in);
				System.out.println("Please enter a value...");
				int v=hill.nextInt();
				while(v%2==0) {
					System.out.println(v);
				v++;
				System.out.println(v);
				break;}
				
			}
}
